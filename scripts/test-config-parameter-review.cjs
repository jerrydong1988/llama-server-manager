const assert = require('node:assert/strict')
const fs = require('node:fs')
const path = require('node:path')

const root = path.resolve(__dirname, '..')
const read = relative => fs.readFileSync(path.join(root, relative), 'utf8')

const configPage = read('src/components/ConfigPage.tsx')
const shared = read('src/components/ConfigPage/shared.tsx')
const search = read('src/components/ConfigPage/ParameterSearch.tsx')
const panel = read('src/components/ConfigPage/ConfigChangePanel.tsx')
const directory = read('src/components/ConfigPage/ConfigDirectory.tsx')
const workspace = read('src/components/ConfigPage/configWorkspace.ts')
const hook = read('src/components/ConfigPage/useEngineCompatibility.ts')
const server = read('src-tauri/src/commands/server.rs')
const types = read('src/store/types.ts')

assert.doesNotMatch(configPage, /_matchedElements/, 'ConfigPage must not use the racy module-level search registry')
assert.doesNotMatch(shared, /_matchedElements/, 'shared controls must not use a module-level search registry')
assert.match(search, /event\.key === 'Enter'/, 'Enter must navigate parameter search results')
assert.match(search, /event\.shiftKey \? -1 : 1/, 'Shift+Enter must navigate backwards')
assert.match(search, /event\.key === 'Escape'/, 'Escape must clear parameter search')
assert.match(search, /querySelectorAll<HTMLElement>\('\[data-config-search-match="true"\]'/, 'search must collect rendered matches in DOM order')
assert.match(shared, /data-config-field=\{fieldKey\}/, 'fields must expose stable keys for locate actions')
assert.match(shared, /ChangedMarker visible=\{changed\}/, 'unsaved edits must use a textual field marker')
assert.match(shared, /EmittedMarker visible=\{emitted\}/, 'emitted parameters must use a textual field marker')
assert.match(shared, /data-config-emitted=\{emitted \? 'true' : undefined\}/, 'emitted fields must expose stable browser-test metadata')
assert.match(shared, /emittedKeys: emittedParams \|\| EMPTY_FIELD_KEYS/, 'section context must provide backend-confirmed emitted fields')
assert.doesNotMatch(shared, /ChangedMarker[\s\S]{0,300}emerald/, 'the changed marker must not restore green parameter semantics')
assert.doesNotMatch(shared, /active: fieldKey/, 'stable field identity must not reuse the former active/green semantic')
assert.doesNotMatch(directory, /changedCount|emittedCount|changedLabel|emittedLabel/, 'the parameter directory must stay a clean navigation list without status counters')
assert.doesNotMatch(directory, /<Badge/, 'the parameter directory must not stack status badges')
assert.match(workspace, /kv_unified_mode: \['kv_unified_mode', 'kv_unified'\]/, 'legacy KV aliases must collapse into one review field')
assert.match(workspace, /canonicalConfigFields/, 'review counts must deduplicate compatibility aliases')
assert.match(workspace, /restoreReviewField/, 'single-field undo must restore every compatibility alias')
assert.match(configPage, /restoreReviewField\(current, savedBaseline, key\)/, 'the review panel must use alias-aware undo')

assert.match(configPage, /commandPreview\?\.emittedOverrideKeys/, 'emission counts must consume backend preview metadata')
assert.match(configPage, /emittedMarker: labels\.changeWillEmit/, 'field markers must use an explicit will-emit label')
assert.match(panel, /'changes' \| 'emitted'/, 'review panel must separate draft changes from emitted parameters')
assert.match(panel, /onLocate/, 'review rows must support locating their field')
assert.match(panel, /onUndo/, 'change rows must support a single-field undo')
assert.doesNotMatch(panel, /slice\(0, 8\)/, 'review panel must not hide changes after the eighth row')

assert.match(server, /fn emitted_override_keys\(/, 'backend must derive emission metadata from generated commands')
assert.match(server, /fn canonical_override_key\(/, 'backend emission metadata must collapse compatibility aliases')
assert.match(server, /emitted_override_keys:/, 'command preview response must serialize emission metadata')
assert.match(types, /emittedOverrideKeys: Array<keyof InstanceConfig>/, 'frontend IPC type must expose emission metadata')
assert.match(hook, /setCommandPreview\(result\)/, 'live compatibility preview must retain backend command metadata')

console.log('Config parameter review and search navigation regression checks passed.')
